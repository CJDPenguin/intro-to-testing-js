// sayHello function
function sayHello(name) {
    if (name === "Alex") {
        return "Hello, Alex!";
    } else {
        return "Hello, Jane!";
    }
}

// plusOne function
function plusOne (num) {
    if(typeof num === "number") {
        return num + 1;
    } return 0;
}